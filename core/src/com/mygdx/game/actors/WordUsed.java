package com.mygdx.game.actors;

public class WordUsed {
	String slowo;
	int ilosc;
	
	public int getIlosc() {
		return ilosc;
	}
	public void setIlosc(int ilosc) {
		this.ilosc = ilosc;
	}
	public void addIlosc(){
		ilosc++;
	}
	public String getSlowo() {
		return slowo;
	}
	public void setSlowo(String slowo) {
		this.slowo = slowo;
	}
}
